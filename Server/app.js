let createError = require('http-errors');
let express = require('express');
let path = require('path');
let cookieParser = require('cookie-parser');
let logger = require('morgan');

let indexRouter = require('./routes/index');
let usersRouter = require('./routes/users');
let documentsRouter = require('./routes/documents');
let videosRouter = require('./routes/videos');
let collectsRouter = require('./routes/collects');
let notesRouter = require('./routes/notes');
let testsRouter = require('./routes/tests');


let app = express();

// view engine setup
app.set('views', path.join(__dirname, 'views'));
app.set('view engine', 'jade');

app.use(logger('dev'));
app.use(express.json());
app.use(express.urlencoded({ extended: false }));
app.use(cookieParser());
app.use(express.static(path.join(__dirname, 'public')));

app.use('/', indexRouter);
app.use('/users', usersRouter);
app.use('/documents', documentsRouter);
app.use('/videos', videosRouter);
app.use('/collects', collectsRouter);
app.use('/notes', notesRouter);
app.use('/tests', testsRouter);
